import { use, useEffect, useState } from "react";
import Swal from "sweetalert2";
import { AuthContext } from "../../Context/AuthContext";
import useAxiosSecure from "../../Hooks/useAxiosSecure";

const MyBids = () => {
  const { user } = use(AuthContext);

  const [bids, setBids] = useState([]);

  const axiosSecure = useAxiosSecure();

  useEffect(() => {
    axiosSecure.get(`Bids?email=${user?.email}`).then((data) => {
      console.log(data.data);
      setBids(data.data);
    });
  }, [user, axiosSecure]);

  // useEffect(() => {
  //   if (user?.email) {
  //     fetch(`https://dragon-ball-server.vercel.app//Bids?email=${user?.email}`, {
  //       headers: {
  //         authorization: `Bearer ${user.accessToken}`,
  //       },
  //     })
  //       .then((res) => res.json())

  //       .then((data) => {
  //         console.log("user email", data);

  //         setBids(data);
  //       });
  //   }
  // }, [user]);

  const handleDeleteBid = (_id) => {
    Swal.fire({
      title: "Are you sure?",
      text: "You won't be able to revert this!",
      icon: "warning",
      showCancelButton: true,
      confirmButtonColor: "#3085d6",
      cancelButtonColor: "#d33",
      confirmButtonText: "Yes, delete it!",
    }).then((result) => {
      if (result.isConfirmed) {
        fetch(`https://dragon-ball-server.vercel.app//Bids/${_id}`, {
          method: "DELETE",
        })
          .then((res) => res.json())
          .then((data) => {
            console.log(data);
            if (data.deletedCount) {
              Swal.fire({
                title: "Deleted!",
                text: "Your bid has been deleted.",
                icon: "success",
              });

              const remainingBids = bids.filter((bid) => bid._id !== _id);

              setBids(remainingBids);
            }
          });
      }
    });
  };

  return (
    <div className="mt-20">
      <h1>MY Bids : {bids.length}</h1>
      <table className="table">
        {/* head */}
        <thead>
          <tr>
            <th>SL No.</th>
            <th>Product</th>
            <th>seller</th>
            <th>Bid Price </th>
            <th>Status</th>
            <th>Actions</th>
          </tr>
        </thead>
        <tbody>
          {/* row 1 */}
          {bids?.map((bid, index) => (
            <tr key={index}>
              <td>{index + 1}</td>
              <td>
                <div className="flex items-center gap-3">
                  <div className="avatar">
                    <div className="mask mask-squircle h-12 w-12">
                      <img
                        src="https://img.daisyui.com/images/profile/demo/2@94.webp"
                        alt="Avatar Tailwind CSS Component"
                      />
                    </div>
                  </div>
                  <div>
                    <div className="font-bold">{bid.buyer_name}</div>
                    {/* <div className="text-sm opacity-50">United States</div> */}
                  </div>
                </div>
              </td>
              <td>
                <p>{bid.buyer_email}</p>
              </td>
              <td>{bid.bid_price}</td>
              <td>
                {bid.status === "pending" ? (
                  <div className="badge badge-warning">{bid.status}</div>
                ) : (
                  <div className="badge badge-success">{bid.status}</div>
                )}
              </td>
              <th>
                <button
                  onClick={() => handleDeleteBid(bid._id)}
                  className="btn btn-outline btn-xs"
                >
                  Remove Bid
                </button>
              </th>
            </tr>
          ))}
          {/* row 2 */}
        </tbody>
      </table>
    </div>
  );
};

export default MyBids;
